package cn.edu.pdsu.pojo;

/**
 * 存放统计数据
 * @author Administrator
 */
public class Data {
	private int num;
	private int count;
	private String name;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
}
